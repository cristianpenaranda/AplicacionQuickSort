    private void cmdGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGenerarActionPerformed
        String dato = this.txtCantidad.getText();
        this.mensaje = "";
        if (dato.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar un dato", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            int cantidad = Integer.parseInt(dato);
            if (cantidad > 0) {
                NumerosAleatorios aleatorios = new NumerosAleatorios(1, 999);
                int[] arreglo = new int[cantidad];
                for(int i=0; i<cantidad; i++){
                    arreglo[i] = aleatorios.getRandom();
                }
                this.secuencia = new Secuencia(cantidad + 1);
                this.mensaje += "Arreglo Generado Aleatoriamente: \n";
                for (int i = 0; i < arreglo.length; i++) {
                    this.secuencia.insertar(arreglo[i]);
                    this.mensaje += arreglo[i] + " | ";
                }
                this.txtArea.setText(mensaje);
                this.cmdResolver.setEnabled(true);
                this.cmdGenerar.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar un número mayor que cero (0)", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cmdGenerarActionPerformed

    private void txtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyTyped
        char caracter = evt.getKeyChar();

        // Verificar si la tecla pulsada no es un digito
        if (((caracter < '0')
                || (caracter > '9'))
                && (caracter != '\b' /*corresponde a BACK_SPACE*/)) {
            evt.consume();  // ignorar el evento de teclado
        }
    }//GEN-LAST:event_txtCantidadKeyTyped

