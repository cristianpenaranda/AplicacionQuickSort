    private void cmdResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdResolverActionPerformed
        Sort li = new Sort();
        li.sort(this.secuencia);
        this.iteraciones = li.getIteraciones();
        this.mensaje += "\n\nArreglo Ordenado:\n" + this.secuencia.toString();
        this.txtArea.setText(mensaje);
        JOptionPane.showMessageDialog(this, "Se ha ordenado exitosamente", "EXITO", JOptionPane.PLAIN_MESSAGE, icono);
        this.cmdResolver.setEnabled(false);
        this.cmdExportar.setEnabled(true);
    }//GEN-LAST:event_cmdResolverActionPerformed

    Icon icono2 = new ImageIcon(getClass().getResource("/imagenes/pdf.png"));

    private void cmdExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdExportarActionPerformed
        String ruta = "";
        JFileChooser chooser = new JFileChooser();
        int option = chooser.showSaveDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            ruta = file.toString();
            try {
                FileOutputStream pdf = new FileOutputStream(ruta + ".pdf");
                Document doc = new Document();
                PdfWriter.getInstance(doc, pdf);
                Date date = new Date();
                String fecha = date.getDate() + "/" + (date.getMonth() + 1) + "/" + (date.getYear() + 1900) + " - " + date.getHours() + ":" + date.getMinutes() + ":" + date.getSeconds();
                String titulo = "SOLUCION METODO QUICKSORT\n\nCRISTIAN PEÑARANDA 1151190\nJONNY JAIMES 1151733\nWILHEN FERNEY GUTIÉRREZ 1151667\n" + fecha;
                doc.open();
                Paragraph paragrafoTitulo = new Paragraph(titulo);
                paragrafoTitulo.setAlignment(1);
                doc.add(paragrafoTitulo);
                doc.add(new Paragraph("\n\n" + mensaje));
                doc.add(new Paragraph("\n\nPaso a Paso: \n" + iteraciones));
                doc.close();
                JOptionPane.showMessageDialog(this, "Se ha generado el archivo pdf exitosamente", "Archivo Generado", JOptionPane.PLAIN_MESSAGE, icono2);
                this.cmdExportar.setEnabled(false);
                ProcessBuilder p = new ProcessBuilder();
                p.command("cmd.exe", "/c", ruta + ".pdf");
                p.start();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getStackTrace(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cmdExportarActionPerformed

